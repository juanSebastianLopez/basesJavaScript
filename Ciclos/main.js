// Ciclo For
for (let i = 1; i <= 18; i++) {
    document.write("El valor de I es: " + i +'<br>')
}

// Ciclo While - Siempre hay que inicializar y declarar la Variable

let i = 1;
while (i <= 20) {
    document.write("El valor de I es: " + i +'<br>')
    i++
}

// Ciclo For AL REVEZ
for(let i = 10; i >= 2; i--){
    document.write("El valor de I es: " + i +'<br>')
}

// Ciclo While  AL REVEZ
let e = 10;
while (e >= 1) {
    document.write("El valor de I es: " + e +'<br>')
    e--
}